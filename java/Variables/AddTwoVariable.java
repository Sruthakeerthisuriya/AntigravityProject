package Variables;

public class AddTwoVariable {
	public static void main(String... args) {
		
		// Directly adding the Variable in Console
		int a = 100;
		int b = 20;
		System.out.println(a+b);
		
		// Declaring Single Variable in Console
		int s=100;
		int t=120;
		int r=s+t;
		System.out.println(r);
		
		
		
	}
}

